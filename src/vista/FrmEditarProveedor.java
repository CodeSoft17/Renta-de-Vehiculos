    public void llenarCombo(){
        Conexion con = new Conexion();
        Connection cn;
        PreparedStatement pst;
        ResultSet rs;
        String sql;
        try{
            Class.forName(con.getDriver());
            cn = DriverManager.getConnection(con.getUrl(), con.getUsuario(), con.getClave());
            sql = "select * from proveedor";
            pst = cn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                String name = rs.getString("nombre");
                this.cmbProveedor1.addItem(name);
            }

        }catch(ClassNotFoundException | SQLException e){
            JOptionPane.showMessageDialog(this, e.toString(), "Resultado", JOptionPane.ERROR_MESSAGE);
        }

    }
    
    public void llenarDatos(){       
        Conexion con = new Conexion();
        Connection cn;
        PreparedStatement pst;
        ResultSet rs;
        String sql;
        String busqueda;
        Proveedor pro = new Proveedor();
       try{
            Class.forName(con.getDriver());
            cn = DriverManager.getConnection(con.getUrl(), con.getUsuario(), con.getClave());
            busqueda = String.valueOf(this.cmbProveedor1.getSelectedItem());
            sql = "select * from proveedor where nombre like '" + busqueda + "%'";
            pst = cn.prepareStatement(sql);
            rs = pst.executeQuery();
            while(rs.next()){
                this.txtIdProveedor.setText(String.valueOf(rs.getInt("idProveedor")));
                this.txtnombre1.setText(rs.getString("nombre"));
                this.txtdireccion1.setText(rs.getString("direccion"));
                this.txtemail1.setText(rs.getString("email"));
                this.txtsitio1.setText(rs.getString("sitio"));
                this.txtTelefono.setText(String.valueOf(rs.getInt("telefono")));
            }      
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, e.toString(), "Resultado", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void modificarRegistros(){
        Proveedor pro = new Proveedor();
        ControlProveedor control = new ControlProveedor();
        try{
            pro.setIdProveedor(Integer.valueOf(this.txtIdProveedor.getText()));
            pro.setNombre(this.txtnombre1.getText());
            pro.setDireccion(this.txtdireccion1.getText());
            pro.setEmail(this.txtemail1.getText());
            pro.setSitio(this.txtsitio1.getText());
            pro.setTelefono(Integer.valueOf(this.txtTelefono.getText()));
            
            int respuesta = JOptionPane.showConfirmDialog(this, "Desea modificare el empleado seleccionado?", "Modificar", JOptionPane.YES_NO_OPTION);
            if(respuesta == JOptionPane.OK_OPTION){
                String mensaje = control.modificarProveedor(pro);
                JOptionPane.showMessageDialog(this, mensaje, "Resultado", JOptionPane.INFORMATION_MESSAGE);
            }           
        }catch(HeadlessException e){
            JOptionPane.showMessageDialog(this, e.toString(), "Resultado", JOptionPane.ERROR_MESSAGE);
        }
    }

