    private void btnaceptarMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_btnaceptarMousePressed
        // TODO add your handling code here:
        if(this.txtnombre.getText().isEmpty() || this.txtdireccion.getText().isEmpty() || this.txtemail.getText().isEmpty() || this.txtsitio.getText().isEmpty()
            || this.txtTelefono.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "No se pueden dejar campos vacios", "Error", JOptionPane.INFORMATION_MESSAGE);
        }else{
            ingresarRegistros();
        }
    }//GEN-LAST:event_btnaceptarMousePressed

