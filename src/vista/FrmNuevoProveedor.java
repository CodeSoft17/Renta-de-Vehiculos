    public void ingresarRegistros(){
        Proveedor pro = new Proveedor();
        ControlProveedor control = new ControlProveedor();
        try{
            pro.setNombre(this.txtnombre.getText());
            pro.setDireccion(this.txtdireccion.getText());
            pro.setEmail(this.txtemail.getText());
            pro.setSitio(this.txtsitio.getText());
            pro.setTelefono(Integer.valueOf(this.txtTelefono.getText()));
            String mensaje = control.agregarProveedor(pro);
            JOptionPane.showMessageDialog(this, mensaje, "Resultado", JOptionPane.INFORMATION_MESSAGE);
            this.limpiar();
        }catch(HeadlessException e){
            JOptionPane.showMessageDialog(this, e.toString(), "Resultado", JOptionPane.ERROR_MESSAGE);
        }
    }
    
    public void limpiar(){
        this.txtnombre.setText("");
        this.txtdireccion.setText("");
        this.txtnombre.setText("");
        this.txtsitio.setText("");
        this.txtemail.setText("");
        this.txtTelefono.setText("");
    }

